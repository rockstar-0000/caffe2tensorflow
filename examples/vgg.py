from kaffe.tensorflow import Network

class VGG16(Network):
    def setup(self):
        (self.feed('data')
             .conv(3, 3, 64, 1, 1, 'SAME', name='conv1_1')
             .relu(name='relu1_1')
             .conv(3, 3, 64, 1, 1, 'SAME', name='conv1_2')
             .relu(name='relu1_2')
             .max_pool(2, 2, 2, 2, 'SAME', name='pool1')
             .conv(3, 3, 128, 1, 1, 'SAME', name='conv2_1')
             .relu(name='relu2_1')
             .conv(3, 3, 128, 1, 1, 'SAME', name='conv2_2')
             .relu(name='relu2_2')
             .max_pool(2, 2, 2, 2, 'SAME', name='pool2')
             .conv(3, 3, 256, 1, 1, 'SAME', name='conv3_1')
             .relu(name='relu3_1')
             .conv(3, 3, 256, 1, 1, 'SAME', name='conv3_2')
             .relu(name='relu3_2')
             .conv(3, 3, 256, 1, 1, 'SAME', name='conv3_3')
             .relu(name='relu3_3')
             .max_pool(2, 2, 2, 2, 'SAME', name='pool3')
             .conv(3, 3, 512, 1, 1, 'SAME', name='conv4_1')
             .relu(name='relu4_1')
             .conv(3, 3, 512, 1, 1, 'SAME', name='conv4_2')
             .relu(name='relu4_2')
             .conv(3, 3, 512, 1, 1, 'SAME', name='conv4_3')
             .relu(name='relu4_3')
             .max_pool(2, 2, 2, 2, 'SAME', name='pool4')
             .conv(3, 3, 512, 1, 1, 'SAME', name='conv5_1')
             .relu(name='relu5_1')
             .conv(3, 3, 512, 1, 1, 'SAME', name='conv5_2')
             .relu(name='relu5_2')
             .conv(3, 3, 512, 1, 1, 'SAME', name='conv5_3')
             .relu(name='relu5_3')
             .max_pool(2, 2, 2, 2, 'SAME', name='pool5')
             .fc(4096, name='fc6')
             .relu(name='relu6')
             .fc(4096, name='fc7')
             .relu(name='relu7')
             .fc(1000, name='fc8')
             .softmax(name='prob'))
